  <h6>ARAM.KINESIOTERAPIA</h6>
  <h1>A nossa prioridade é restabelecer a sua saúde e bem-estar. </h1>
  <div>
    Nosso objetivo na ARAM é tratar a sua inflamação, fazer desaparecer a dor e recuperar sua mobilidade,
    mantendo-o em movimento e garantindo que sua reabilitação é eficaz, rápida e tranquila. Desde 2010, ano em que fundámos 
    nossa Academia de reabilitação motora e Kinesioterapia que oferecemos os mais altos padrões de qualidade de tratamento e
    atendimento ao cliente. Somos uma equipa de profissionais dedicados em tratar pacientes com integridade, dedicação e profissionalismo.
    <div>
    No ARAM desenvolvemos a nossa própria metodologia de terapia de reabilitação, combinando técnicas e conhecimentos
    específicos de kinesioterapia, osteopatia e fisioterapia e nutrição para reduzir o tempo de recuperação e aumentar a sua eficácia.
    </div>
    <div>
    Usamos como terapias:
    <ul><li>
    Manipulações osteoarticulares.</li><li>

   Massagem (com metodologia própria).</li><li>

    Movimentos terapêuticos (activos e passivos) expecíficos para cada tipo de patologia.</li><li>

    Electroterapia, muito eficaz no alívio da dor e efeito anti inflamatório.</li><li>

     Lazer, muito eficaz na bioregeneração tecidular e celular.</li><li>

    Crioterapia para casos inflamatórios.</li><li>

    Pressoterapia.</li><li>

    Ondas de choque muito eficaz nos tratamentos em zonas tendinosas, aderências, calcificações.</li></ul>
    </div>
    <div>
    Considerando que a recuperação não se prende apenas em alguns casos na terapia passiva e movimentos, sabemos 
    de antemão que a perda de peso em alguns casos é crucial  para o sucesso da recuperação, como no caso das atroses. 
    Por isso, oferecemos soluçõe de uma boa nutrição para os efeitos desejados, aumentado a massa muscular através dos
     exercícios e perda de peso através de uma alimentação adequada.
     </div>
     <div>

     Na clínica temos também uma vasta gama de produtos complementares que escolhemos criteriosamente para 
     oferecer as marcas que consideramos mais completas e de melhor qualidade para complementar o seu tratamento, 
     todos produtos naturais.
